        private void ReloadPreview(bool updateDirty = true)
        {
            _entManager.DeleteEntity(PreviewDummy);
            PreviewDummy = EntityUid.Invalid;

            if (Profile == null || !_prototypeManager.HasIndex(Profile.Species))
            {
                UpdatePreviewJobLabel(null);
                return;
